package sheff.rjd.ws.OCO.AfterSign;

public class PackSignObject {

	String name;
	int signlvl;
	long id;
	String bldoc;
	
	
	public String getBldoc() {
		return bldoc;
	}
	public void setBldoc(String bldoc) {
		this.bldoc = bldoc;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSignlvl() {
		return signlvl;
	}
	public void setSignlvl(int signlvl) {
		this.signlvl = signlvl;
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	
	
	
	
	
}
